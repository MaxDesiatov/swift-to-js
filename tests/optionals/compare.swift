public func checkEqual(singleOptional: Bool?, with other:Bool?) -> Bool {
	return singleOptional == other
}

public func checkEqual(doubleOptional: Bool??, with other:Bool??) -> Bool {
	return doubleOptional == other
}

public func checkNotEqual(singleOptional: Bool?, with other:Bool?) -> Bool {
	return singleOptional != other
}

public func checkNotEqual(doubleOptional: Bool??, with other:Bool??) -> Bool {
	return doubleOptional != other
}
